# True     : Boolean value representing truth
# False    : Boolean value representing false
# None     : Represents absence of a value

# and      : Logical AND operator
# or       : Logical OR operator
# not      : Logical NOT operator

# if       : Executes code when condition is True
# elif     : Checks another condition if previous fails
# else     : Executes code when all conditions fail

# for      : Iterates over a sequence
# while    : Executes loop while condition is True
# break    : Terminates the loop immediately
# continue : Skips current iteration
# pass     : Placeholder that does nothing

# in       : Checks membership in a sequence
# is       : Checks object identity (same memory)

# def      : Defines a function
# return   : Sends value back from a function
# lambda   : Creates an anonymous function
# yield    : Returns value and pauses function (generator)

# try      : Wraps code that may cause an error
# except   : Handles exceptions
# finally  : Executes code regardless of exception
# raise    : Manually raises an exception
# assert   : Debugging check for condition

# class    : Defines a class
# del      : Deletes object reference

# import   : Imports a module
# from     : Imports specific items from module
# as       : Creates alias for module or import

# global   : Access global variable inside function
# nonlocal : Access enclosing function variable

# with     : Manages resources using context manager
# async    : Declares asynchronous function
# await    : Waits for async operation



